#include "3-calc.h"

/**
 * op_add - adds two ints
 *
 * @a: first int
 * @b: second int
 *
 * Return: sum of the ints
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - minus two ints
 *
 * @a: first int
 * @b: second int
 *
 * Return: difference of the ints
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two ints
 *
 * @a: first int
 * @b: second int
 *
 * Return: product of the ints
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two ints
 *
 * @a: first int
 * @b: second int
 *
 * Return: division of the ints
 */

int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - get modulos of two ints
 *
 * @a: first int
 * @b: second int
 *
 * Return: modulos of the ints
 */

int op_mod(int a, int b)
{
	return (a % b);
}
